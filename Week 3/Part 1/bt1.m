n = input('Input a number from 1 to 9: ');

switch (n)
    case 1
        disp('One')
    case 2
        disp('Two')
    case 3
        disp('Three')
    case 4
        disp('Four')
    case 5
        disp('Five')
    case 6
        disp('Six')
    case 7
        disp('Seven')
    case 8
        disp('Eight')
    case 9
        disp('Nine')
    otherwise
        disp('Can''t read the number')
end